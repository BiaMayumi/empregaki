            <h3 class="texto1">
            Você precisa pensar no conceito de marketing pessoal para aplicar a seu currículo. Essa é<br> essência do que você precisa dominar, já que você está preparando um seu currículo com <br>a finalidade de atrair e convencer um potencial empregador a convocar você uma entrevista,<br> fazendo com que ele tenha interesse em conhecer a pessoa por trás do papel e saber como você <br>pode assumir o cargo que está sendo oferecido, com um bom aproveitamento para a empresa e <br>para você.<br><br><br>

            Estrutura<br> O visual do currículo é a primeira coisa que causa impacto em quem o recebe, pois a imagem já é <br>processada no cérebro antes que a pessoa tenha tempo de ler o texto ou reconhecer algum nome <br>de empresa ou escola que chame a atenção no conteúdo. Então, o formato do currículo é muito <br>importante porque o leitor subconscientemente processa a imagem primeiro. Seja qual for a<br> finalidade ou tipo de currículo que você deseja elaborar, ele precisa ser bem estruturado, de fácil <br>leitura e padronizado. Isso vale tanto para quem está entrando no mercado de trabalho agora <br>quanto para um profissional bem qualificado. No momento de apresentar a sua candidatura aos <br>representantes de RH de uma empresa, é fundamental que a primeira impressão que o seu<br> currículo cause é de que você tem competência e organização. <br><br><br>

            Margens<br>De acordo com Associação Brasileira de Normas Técnicas (ABNT). As margens devem conter 3cm <br>no lado superior e esquerdo, e 2cm para o lado direito e inferior da página. <br><br><br>


            Alinhamento<br>O alinhamento do currículo é um processo bastante simples, mas quase sempre deixados de lado-<br> porque normalmente já fica do jeito que a maioria do mundo ocidental está acostumada a ler. <br>Cabeçalhos, níveis de experiência, informações educacionais e habilidades adicionais ou qualquer <br>outra seção são alinhadas à esquerda do documento...
